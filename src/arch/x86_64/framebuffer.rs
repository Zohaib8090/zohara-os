/// Check if the framebuffer is available and initialized.
pub fn framebuffer_available() -> bool {
    unsafe { FB_INITIALIZED }
}

pub fn framebuffer_addr() -> usize { unsafe { FB_ADDR } }
pub fn framebuffer_width() -> usize { unsafe { FB_WIDTH } }
pub fn framebuffer_height() -> usize { unsafe { FB_HEIGHT } }
pub fn framebuffer_bpp() -> u8 { unsafe { FB_BPP } }

/// Parse framebuffer info from multiboot info structure.
/// Must be called early in kernel_main, before any println!.
pub fn framebuffer_init() {
    unsafe {
        extern "C" {
            static mb_info: u64;
        }
        let info_addr = mb_info as usize;
        if info_addr == 0 {
            return;
        }

        let info = info_addr as *const u8;
        let flags = core::ptr::read_volatile(info.add(0) as *const u32);

        // Bit 12 = framebuffer info valid
        if flags & (1 << 12) == 0 {
            return;
        }

        FB_ADDR = core::ptr::read_volatile(info.add(FB_ADDR_OFF) as *const u64) as usize;
        FB_PITCH = core::ptr::read_volatile(info.add(FB_PITCH_OFF) as *const u32) as usize;
        FB_WIDTH = core::ptr::read_volatile(info.add(FB_WIDTH_OFF) as *const u32) as usize;
        FB_HEIGHT = core::ptr::read_volatile(info.add(FB_HEIGHT_OFF) as *const u32) as usize;
        FB_BPP = core::ptr::read_volatile(info.add(FB_BPP_OFF) as *const u8);

        if FB_ADDR == 0 || FB_WIDTH == 0 || FB_HEIGHT == 0 {
            return;
        }

        // Only support 32-bit direct RGB (type 1)
        let fb_type = core::ptr::read_volatile(info.add(FB_TYPE_OFF) as *const u8);
        if fb_type != 1 {
            return;
        }

        // Map framebuffer through page tables (it's typically above 1GB).
        // Identity-map the framebuffer pages so we can write to physical addresses.
        let fb_size = FB_PITCH * FB_HEIGHT;
        let page_base = FB_ADDR & !0xFFF;
        let page_count = (fb_size + 0xFFF) / 0x1000;
        for i in 0..page_count {
            let addr = page_base + i * 0x1000;
            crate::paging::map_page(addr);
        }

        // Clear screen to black
        framebuffer_clear(0x000000);
        FB_ROW = 0;
        FB_COL = 0;
        FB_INITIALIZED = true;
    }
}

/// Fill the entire framebuffer with a solid color.
pub fn framebuffer_clear(color: u32) {
    unsafe {
        if !FB_INITIALIZED && FB_ADDR == 0 {
            return;
        }
        let fb = FB_ADDR as *mut u8;
        let pixel_bytes = (FB_BPP / 8) as usize;
        let row_bytes = FB_PITCH;

        for y in 0..FB_HEIGHT {
            let row_start = y * row_bytes;
            for x in 0..FB_WIDTH {
                let offset = row_start + x * pixel_bytes;
                // BGRA format: B=0, G=1, R=2, A=3
                core::ptr::write_volatile(fb.add(offset), (color & 0xFF) as u8);         // B
                core::ptr::write_volatile(fb.add(offset + 1), ((color >> 8) & 0xFF) as u8); // G
                core::ptr::write_volatile(fb.add(offset + 2), ((color >> 16) & 0xFF) as u8); // R
                if pixel_bytes == 4 {
                    core::ptr::write_volatile(fb.add(offset + 3), 0xFF); // A
                }
            }
        }
    }
}

/// Write one pixel at (x, y) with the given color (0xRRGGBB).
pub fn framebuffer_put_pixel(x: usize, y: usize, color: u32) {
    unsafe {
        if x >= FB_WIDTH || y >= FB_HEIGHT {
            return;
        }
        let fb = FB_ADDR as *mut u8;
        let pixel_bytes = (FB_BPP / 8) as usize;
        let offset = y * FB_PITCH + x * pixel_bytes;
        core::ptr::write_volatile(fb.add(offset), (color & 0xFF) as u8);
        core::ptr::write_volatile(fb.add(offset + 1), ((color >> 8) & 0xFF) as u8);
        core::ptr::write_volatile(fb.add(offset + 2), ((color >> 16) & 0xFF) as u8);
        if pixel_bytes == 4 {
            core::ptr::write_volatile(fb.add(offset + 3), 0xFF);
        }
    }
}

const CHAR_W: usize = 8;
const CHAR_H: usize = 16;
const FG_COLOR: u32 = 0x00FF00; // Green on black
const BG_COLOR: u32 = 0x000000;

/// Render one ASCII character at the current cursor position.
/// Handles newline, carriage return, and scrolling.
pub fn framebuffer_put_char(ch: u8) {
    unsafe {
        if !FB_INITIALIZED {
            return;
        }

        match ch {
            b'\n' => {
                FB_COL = 0;
                FB_ROW += 1;
            }
            b'\r' => {
                FB_COL = 0;
            }
            _ => {
                if ch >= 32 && ch <= 126 {
                    let glyph = &FONT[(ch - 32) as usize];
                    let start_x = FB_COL * CHAR_W;
                    let start_y = FB_ROW * CHAR_H;

                    for gy in 0..CHAR_H {
                        let row_bits = glyph[gy];
                        for gx in 0..CHAR_W {
                            let color = if row_bits & (0x80 >> gx) != 0 {
                                FG_COLOR
                            } else {
                                BG_COLOR
                            };
                            framebuffer_put_pixel(start_x + gx, start_y + gy, color);
                        }
                    }
                }
                FB_COL += 1;
            }
        }

        let cols = FB_WIDTH / CHAR_W;
        let rows = FB_HEIGHT / CHAR_H;

        if FB_COL >= cols {
            FB_COL = 0;
            FB_ROW += 1;
        }

        if FB_ROW >= rows {
            // Scroll: copy pixel rows up by CHAR_H
            let scroll_bytes = (rows - 1) * CHAR_H * FB_PITCH;
            let fb = FB_ADDR as *mut u8;
            core::ptr::copy(fb.add(CHAR_H * FB_PITCH), fb, scroll_bytes);

            // Clear last row
            let last_row_start = (rows - 1) * CHAR_H;
            for y in last_row_start..(rows * CHAR_H) {
                let row_start = y * FB_PITCH;
                for x in 0..FB_WIDTH {
                    let offset = row_start + x * 4;
                    core::ptr::write_volatile(fb.add(offset), 0);
                    core::ptr::write_volatile(fb.add(offset + 1), 0);
                    core::ptr::write_volatile(fb.add(offset + 2), 0);
                    core::ptr::write_volatile(fb.add(offset + 3), 0xFF);
                }
            }
            FB_ROW = rows - 1;
        }
    }
}

/// Adapter for core::fmt::Write
pub struct FramebufferWriter;

impl fmt::Write for FramebufferWriter {
    fn write_str(&mut self, s: &str) -> fmt::Result {
        for byte in s.bytes() {
            framebuffer_put_char(byte);
        }
        Ok(())
    }
}
